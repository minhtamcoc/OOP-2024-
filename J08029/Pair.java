public class Pair {
    private int x;
    private int y;
    private int cnt;
    public Pair(int x,int y,int cnt){
        this.x=x;
        this.y=y;
        this.cnt=cnt;
    }
    public int getX(){
        return x;
    }
    public int getY(){
        return y;
    }
    public int getCNT(){
        return cnt;
    }
}
