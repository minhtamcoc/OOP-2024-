public class MonHoc {
    private String id;
    private String name;
    private String tinchi;
    public MonHoc(String id,String name,String tinchi){
        this.id=id;
        this.name=name;
        this.tinchi=tinchi;
    }
    public String getID(){
        return id;
    }
    public String getName(){
        return name;
    }
}
