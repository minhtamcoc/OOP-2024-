public class MonHoc {
    private String id;
    private String name;
    private int sotin;
    public MonHoc(String id,String name,int sotin){
        this.id=id;
        this.name=name;
        this.sotin=sotin;
    }
    public String getId(){
        return id;
    }
    public String getName(){
        return name;
    }
}