public class Hang {
    private String id;
    private String name;
    private String xeploai;
    public Hang(String id,String name,String xeploai){
        this.id=id;
        this.name=name;
        this.xeploai=xeploai;
    }
    public String getId(){
        return id;
    }
    public String getName(){
        return name;
    }
    public String getXL(){
        return xeploai;
    }
}
