public class MonThi {
    private String idM;
    private String nameM;
    private String hinhthuc;
    public MonThi(String idM,String nameM,String hinhthuc){
        this.idM=idM;
        this.nameM=nameM;
        this.hinhthuc=hinhthuc;
    }
    public String getIDM(){
        return idM;
    }
    public String getNameM(){
        return nameM;
    }
}
